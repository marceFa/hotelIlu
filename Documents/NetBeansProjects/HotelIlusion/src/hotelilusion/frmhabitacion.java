    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtnumero.getText().length() ==0) {
            JOptionPane.showConfirmDialog(rootPane, "Por favor ingrese un número de habitación");
            txtnumero.requestFocus();
        }
        
        if (txtprecio_diario.getText().length() ==0) {
            JOptionPane.showConfirmDialog(rootPane, "Por favor ingrese el precio diario de la habitación");
            txtprecio_diario.requestFocus();
        }
        
        Habitacion habitacion=new Habitacion();
        Habitacion func = new Habitacion();
        
        habitacion.setNumero(Integer.parseInt(txtnumero.getText()));
        int seleccionado = jComboBoxpiso.getSelectedIndex();
        habitacion.setPiso(Integer.parseInt(jComboBoxpiso.getItemAt(seleccionado)));
        habitacion.setCaracteristicas(jEditorPanecaract.getText());
        habitacion.setCant_personas(Integer.parseInt(txtcantpersonas.getText()));
        habitacion.setCant_de_camas(Integer.parseInt(txtcantcamas.getText()));
        habitacion.setPrecio_diario(Double.parseDouble(txtprecio_diario.getText()));
                
        seleccionado = jComboBoxestado.getSelectedIndex();
        habitacion.setEstado(Boolean.parseBoolean(jComboBoxestado.getToolTipText()));
        
        if (accion.equals("guardar")) {
            if(func.guardarHabitacion(habitacion)) {
                JOptionPane.showMessageDialog(rootPane, "La habitación fue registrada");
                mostrar("");
                inhabilitar();
            } else if (accion.equals("actualizar")) {
                habitacion.getId_habitacion(Integer.parseInt(txtid_habitacion.getText()));
                
        
             if (func.actualizar(habitacion)) {
                 JOptionPane.showMessageDialog(rootPane, "La habitación fue actualizada correctamente");
                mostrar("");
                inhabilitar();
                 
             }
                
                
                
            }
        }
        
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        
        if (!txtid_habitacion.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea eliminar la habitación", "Confirmar",2);
            
            if(confirmacion ==0) {
                
                Habitacion func = new Habitacion();
                Habitacion habitacion=new Habitacion();
                
                habitacion.setId_habitacion(Integer.parseInt(txtid_habitacion.getText()));
                func.eliminar(habitacion);
                mostrar("");
                inhabilitar();
            }
            
        }
        
    }//GEN-LAST:event_btneliminarActionPerformed

