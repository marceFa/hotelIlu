    void mostrar (String buscar) {
        try {
            DefaultTableModel modelo;
            Habitacion func = new Habitacion();
            modelo = func.mostrar(buscar);
            tabladatos.setModel(modelo);
            ocultar_columnas();
            jLabeltotalregistros.setText("Total registros " + Integer.toString(func.totalregistros));
            
        }   catch (Exception e) {
        JOptionPane.showConfirmDialog(rootPane, e);
        
        
        
        }
        int numero=Integer.parseInt(txtnumero.getText());
        int piso=Integer.parseInt(jComboBoxpiso.getItemAt(0));
        String caracteristicas=(jEditorPanecaract.getText());
        int cant_personas=Integer.parseInt(txtcantpersonas.getText());
        int cant_camas=Integer.parseInt(txtcantcamas.getText());
        double precio_diario=Double.parseDouble(txtprecio_diario.getText());
        boolean estado=Boolean.parseBoolean(jComboBoxestado.getItemAt(0));
        
        Habitacion habitacion=new Habitacion(numero, piso, caracteristicas, cant_personas, cant_camas,precio_diario, estado);
        habitacionData.guardarHabitacion(habitacion);
        
    }

