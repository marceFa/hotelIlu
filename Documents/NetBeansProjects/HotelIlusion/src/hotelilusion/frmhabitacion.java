    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
        // TODO add your handling code here:
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";
        
        int fila = tabladatos.rowAtPoint(evt.getPoint());
        txtid_habitacion.setText(tabladatos.getValueAt(fila, 0).toString());
        txtnumero.setText(tabladatos.getValueAt(fila, 1).toString());
        jComboBoxpiso.setSelectedItem(tabladatos.getValueAt(fila,3).toString());
        jEditorPanecaract.setText(tabladatos.getValueAt(fila, 4).toString());
        txtcantpersonas.setText(tabladatos.getValueAt(fila, 5).toString());
        txtcantcamas.setText(tabladatos.getValueAt(fila, 6).toString());
        txtprecio_diario.setText(tabladatos.getValueAt(fila, 7).toString());
        txtid_tipohab.setText(tabladatos.getValueAt(fila, 8).toString());
        jComboBoxestado.setSelectedItem(tabladatos.getValueAt(fila, 9).toString());
        
    }//GEN-LAST:event_tabladatosMouseClicked

